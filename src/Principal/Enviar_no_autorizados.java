     public void llenar() {
        try {
            String nombre;
            conexion_facturacion n = new conexion_facturacion("root", "manager");
            n.conectar();

            nombre = "SELECT idxml,Estado_factura,numero,motivo_no_autorizado,fecha FROM adv_xml.xml_no_autorizados,adv_facturacion.factura where xml_factura=idfactura and Estado_factura='NO AUTORIZADO' ;";


            ResultSet consultan = n.consulta(nombre);

            Object datos[] = new Object[5];

            while (consultan.next()) {
                for (int i = 0; i < 5; i++) {






                    datos[i] = consultan.getObject(i + 1);


                    //datos[i] = consultac.getObject(i + 1);
                }
                litabla.addRow(datos);
            }

            tabla.setModel(litabla);
            
            n.coneccion.close();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(enviar_facturas_contingencia.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(enviar_facturas_contingencia.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
