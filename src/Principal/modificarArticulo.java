    private void codActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_codActionPerformed
        String descripcion = "";
        int sM = 0, sm = 0, sa = 0;
        try {
            model = new ConnectionTableDB(usu, contra, "adv_facturacion", "SELECT nombre,stock_maximo,stock_minimo,stock_actual,punit,descuento,tipo_producto_idtipo_producto FROM producto WHERE codigo_barra='" + cod.getText() + "'", false);
            try {
                if (model.getRowCount() != 0) {
                    descripcion = (String) model.getValueAt(0, 0);
                    sM = Integer.valueOf(String.valueOf(model.getValueAt(0, 1)));
                    sm = Integer.valueOf(String.valueOf(model.getValueAt(0, 2)));
                    sa = Integer.valueOf(String.valueOf(model.getValueAt(0, 3)));
                    Double pu = Double.valueOf(String.valueOf(model.getValueAt(0, 4)));
                    Double descuen = Double.valueOf(String.valueOf(model.getValueAt(0, 5)));
                    int tip = Integer.parseInt(String.valueOf(model.getValueAt(0, 6)));
                    if (tip == 2) {
                        Sact.setText(sa + "");
                        Smax.setText(sM + "");
                        Smin.setText(sm + "");
                        unitario.setText(pu + "");
                        descuento.setText(descuen + "");
                        desc.setText(descripcion);
                        cod.setEditable(false);
                        Sact.setEnabled(true);
                        Smax.setEnabled(true);
                        Smin.setEnabled(true);
                    } else {
                        Sact.setText("0");
                        Smax.setText("1");
                        Smin.setText("0");
                        unitario.setText(pu + "");
                        descuento.setText(descuen + "");
                        desc.setText(descripcion);
                        cod.setEditable(false);
                        Sact.setEnabled(false);
                        Smax.setEnabled(false);
                        Smin.setEnabled(false);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "ese articulo no existe");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "ese articulo no existe");
                cod.setText("");
            }
        } catch (SQLException ex) {
            System.err.println(ex.getMessage());
        }
        desc.requestFocus();  // TODO add your handling code here:
    }//GEN-LAST:event_codActionPerformed

